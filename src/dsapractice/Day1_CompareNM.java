package dsapractice;

//P1-Decision making in java(Basic)
public class CompareNM {
	public static String compareNM(int n, int m) {
	       
	        if(n<m){
	            return "lesser";
	        }
	        else if(n==m){
	            return "equal";
	        }
	        else{
	            return "greater";
	        }
	    }
	}

