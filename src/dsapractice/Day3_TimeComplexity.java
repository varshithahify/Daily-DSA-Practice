package dsapractice;

public class Day3_TimeComplexity {
	 public static void TimeComplexity(int n) {
	        switch(n){
	            case 1:
	                System.out.println("logn");
	                break;
	            case 2:
	                System.out.println("nlogn");
	                break;
	            case 3:
	                System.out.println("n^2");
	                break;
	            case 4:
	                System.out.println("n^2");
	                break;
	            default:
	                System.out.println("Invalid");
	        }
	    }
}
